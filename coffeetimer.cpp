int HandleEvent(const Event& e)
{
    switch(e.type) {
        case INIT: {
            EnterWaitingState();
            break;
        }

        case SHORT_PRESS: {
            if(debugEvents) printf("SHORT_PRESS %d\n", e.data);
            switch(appState) {
                case STATE_DARK:
                    EnterWaitingState();
                    break;
                case STATE_WAITING:
                    CancelTimer(waitingTimer);
                    waitingTimer = -1;
                    if(e.data == BUTTON_1) {
                        EnterRunningState(0);
                    } else if(e.data == BUTTON_2) {
                        EnterRunningState(1);
                    } else {
                        abort();
                    }
                    // XXX enter timer
                    break;
                case STATE_RUNNING:
                    abort(); // implement me
                    break;
                case STATE_PAUSED:
                    abort(); // implement me
                    break;
                case STATE_ALARM:
                    CancelClip(beepClip);
                    CancelTimer(alarmTimer);
                    alarmTimer = -1;
                    CancelTimer(alarmStepTimer);
                    alarmStepTimer = -1;
                    EnterWaitingState();
                    break;
            }
            break;
        }

        case LONG_PRESS: {
            if(debugEvents) printf("LONG_PRESS %d\n", e.data);
            switch(appState) {
                case STATE_DARK:
                    EnterWaitingState();
                    break;
                case STATE_WAITING:
                    // nothing now for long press at WAITING
                    break;
                case STATE_RUNNING:
                    abort(); // implement me
                    break;
                case STATE_PAUSED:
                    abort(); // implement me
                    break;
                case STATE_ALARM:
                    abort(); // implement me
                    break;
            }
            break;
        }

        case TIMER_FINISHED: {
            if(debugEvents) printf("TIMER_FINISHED %d\n", e.data);
            switch(appState) {
                case STATE_DARK:
                    // Won't Happen
                    break;
                case STATE_WAITING:
                    printf("e.data = %d\n", e.data);
                    printf("waitingTimer = %d\n", waitingTimer);
                    printf("runningTimer = %d\n", runningTimer);
                    printf("alarmTimer = %d\n", alarmTimer);
                    printf("alarmStepTimer = %d\n", alarmStepTimer);
                    assert(e.data == waitingTimer);
                    waitingTimer = -1;
                    EnterDarkState();
                    break;
                case STATE_RUNNING:
                    assert(e.data == runningTimer);
                    runningTimer = -1;
                    EnterAlarmState();
                    break;
                case STATE_PAUSED:
                    abort(); // implement me
                    break;
                case STATE_ALARM:
                    if(e.data == alarmTimer) {
                        CancelTimer(alarmStepTimer);
                        alarmStepTimer = -1;
                        CancelClip(beepClip);
                        beepClip = -1;
                        EnterWaitingState();
                    } else if(e.data == alarmStepTimer) {
                        if(alarmStateStep == ALARM_BEEP) {
                            AlarmStateQuiet();
                        } else if(alarmStateStep == ALARM_QUIET) {
                            AlarmStateBeep();
                        }
                        UpdateAlarmState();
                    } else {
                        abort();
                    }
                    break;
            }
            break;
        }

        case TIMER_TICK: {
            if(debugEvents) printf("TIMER_TICK %d\n", e.data);
            switch(appState) {
                case STATE_DARK:
                    // Won't Happen
                    break;
                case STATE_WAITING:
                    assert(e.data == waitingTimer);
                    UpdateWaitingState();
                    break;
                case STATE_RUNNING:
                    assert(e.data == runningTimer);
                    UpdateRunningState();
                    break;
                case STATE_PAUSED:
                    abort(); // implement me
                    break;
                case STATE_ALARM:
                    // Nothing
                    break;
            }
            break;
        }

        case CLIP_FINISHED: {
            if(debugEvents) printf("CLIP_FINISHED %d\n", e.data);
            switch(appState) {
                case STATE_DARK:
                    // Won't Happen
                    break;
                case STATE_WAITING:
                    // Won't Happen
                    break;
                case STATE_RUNNING:
                    // Won't Happen
                    break;
                case STATE_PAUSED:
                    // Won't Happen
                    break;
                case STATE_ALARM:
                    beepClip = -1;
                    break;
            }
            break;
        }
    }
    return 0;
}
